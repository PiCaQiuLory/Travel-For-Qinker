package com.ss.pojo.trip;

import java.io.Serializable;

public class City implements Serializable{
	
	private int id;
	private String name;
	private int s_id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	
	

}
