package com.ss.pojo.trip;

public class UserSettle {
	
	private int id;
	
	private String nickname;
	
	private int singularNumber;
	
	private int finishNumber;
	
	private Float income;
	
	private Float forecastProfit;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public int getSingularNumber() {
		return singularNumber;
	}

	public void setSingularNumber(int singularNumber) {
		this.singularNumber = singularNumber;
	}

	public int getFinishNumber() {
		return finishNumber;
	}

	public void setFinishNumber(int finishNumber) {
		this.finishNumber = finishNumber;
	}

	public Float getIncome() {
		return income;
	}

	public void setIncome(Float income) {
		this.income = income;
	}

	public Float getForecastProfit() {
		return forecastProfit;
	}

	public void setForecastProfit(Float forecastProfit) {
		this.forecastProfit = forecastProfit;
	}
	

	
}
